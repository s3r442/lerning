print("Melons"+'Melons')
#str записывается single или double quotes
print("Your's melons")
print('beauty "melons"')
#если используем внутри str single или double quotes ограждаем конструкцию оставшимися
print('Your\'s beauty "melons"')
#если внутри str используем оба вида quotes, то один из видов выделяем знаком \ и им же ограждаем конструкцию
print('melon'+'melon')
#несколько str в одном statement можно записывать с помощью знака +
print(10*'melon')
#str можно умножать *
print('melon\nmelon')
#\n - new line
print(r'melon\nmelon')
#(r'.....') - raw string - делает всю строку литеральной
print(2*3)
#multiplication
print(2**3)
#degree
print(20/3)
#division (float)
print(20//3)
#floor division (int)
print((8-3)*4)
#bracket - дает приоритет в выражениях
print(3%2)
#% - возвращает остаток после division
